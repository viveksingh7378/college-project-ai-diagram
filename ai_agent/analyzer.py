def validate_html_fix(rel_path):
    """
    Validate an HTML file after the AI has applied fixes.
    Returns (ok: bool, reason: str).

    Checks:
      1. No unknown / hallucinated tag names (e.g. <jt>, <iv>)
      2. <div> open/close count is balanced
      3. Required skeleton tags are present (DOCTYPE, html, head, body)
    """
    import re as _re

    full_path = os.path.join(PROJECT_ROOT, rel_path)
    try:
        with open(full_path, "r", encoding="utf-8", errors="replace") as f:
            content = f.read()
    except OSError as e:
        return False, f"Cannot read file: {e}"

    # ── 1. Unknown tag check ──────────────────────────────────────
    # Extract every <tagname ...> and </tagname> — skip comments and DOCTYPE
    tag_names = _re.findall(r"</?([A-Za-z][A-Za-z0-9-]*)", content)
    unknown = {t for t in tag_names if t.lower() not in _VALID_HTML_TAGS}
    if unknown:
        return False, (
            f"AI introduced invalid/unknown HTML tag(s): {sorted(unknown)}. "
            "This is a hallucination — reverting the file."
        )

    # ── 2. <div> balance check ────────────────────────────────────
    div_opens  = len(_re.findall(r"<div[\s>]", content, _re.IGNORECASE))
    div_closes = content.lower().count("</div>")
    if div_opens != div_closes:
        return False, (
            f"<div> imbalance after fix: {div_opens} open vs {div_closes} close. "
            "Reverting."
        )

    # ── 3. Required skeleton tags ─────────────────────────────────
    lower = content.lower()
    for needle in ("<!doctype", "<html", "</html>", "<head", "</head>",
                   "<body", "</body>"):
        if needle not in lower:
            return False, f"Required tag '{needle}' missing after fix — reverting."

    return True, "OK"


def extract_json(text):
    """Strip markdown fences and extract the outermost {...} JSON object."""
    import re
    text = re.sub(r"```(?:json)?\s*", "", text)
    text = text.replace("```", "").strip()
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass
    match = re.search(r"\{.*\}", text, re.DOTALL)
    if match:
        try:
            return json.loads(match.group(0))
        except json.JSONDecodeError:
            pass
    return None


def run_ai_batched(files_dict, providers):
    """
    Split files into size-bounded batches and call the AI providers once per
    batch. This replaces the old "send every file in one huge prompt" path
    which blew past Groq's per-request token budget and caused:
        400 — Please reduce the length of the messages or completion.

    providers is a list of (name, fn) tuples tried in order for each batch.
    Each fn(prompt) returns the raw response text, or None on failure.

    Returns a single merged analysis dict with all issues from all batches.
    """
    # 1. Pack files into batches no bigger than MAX_BATCH_CHARS
    batches = []
    current = {}
    current_chars = 0
    for label, meta in files_dict.items():
        size = len(meta["content"])
        # If a single chunk is already bigger than the batch limit, give it
        # its own batch (it still has to fit under the provider's hard limit,
        # but that's the chunker's job, not ours).
        if size >= MAX_BATCH_CHARS:
            if current:
                batches.append(current)
                current = {}
                current_chars = 0
            batches.append({label: meta})
            continue
        if current and current_chars + size > MAX_BATCH_CHARS:
            batches.append(current)
            current = {}
            current_chars = 0
        current[label] = meta
        current_chars += size
    if current:
        batches.append(current)

    print(f"  Packed {len(files_dict)} chunk(s) into {len(batches)} batch(es) "
          f"(max {MAX_BATCH_CHARS} chars each)")

    # 2. Call each provider per batch; collect all issues
    all_issues = []
    summaries = []
    any_batch_succeeded = False

    for i, batch in enumerate(batches, 1):
        # Wall-clock guard — bail out gracefully if we're out of time.
        if _RUN_START_TIME is not None:
            elapsed = time.time() - _RUN_START_TIME
            if elapsed > AI_ANALYSIS_BUDGET_SECS:
                print(f"\n  ⏱  Wall-clock budget exhausted "
                      f"({elapsed:.0f}s / {AI_ANALYSIS_BUDGET_SECS}s) — "
                      f"stopping after batch {i-1}/{len(batches)}")
                break

        batch_chars = sum(len(m["content"]) for m in batch.values())
        print(f"\n  ── Batch {i}/{len(batches)} "
              f"({len(batch)} file(s), {batch_chars} chars) ──")
        prompt = build_prompt(batch)

        raw = None
        for name, fn in providers:
            print(f"  Trying {name}...")
            raw = fn(prompt)
            if raw:
                break

        if not raw:
            print(f"  ⚠ Batch {i} — all providers failed; skipping this batch")
            continue

        analysis = extract_json(raw)
        if not analysis:
            print(f"  ⚠ Batch {i} — response wasn't valid JSON; skipping")
            continue

        any_batch_succeeded = True
        batch_issues = analysis.get("issues", []) or []
        all_issues.extend(batch_issues)
        if analysis.get("summary"):
            summaries.append(analysis["summary"])
        print(f"  ✓ Batch {i} — {len(batch_issues)} issue(s) found")

    if not any_batch_succeeded:
        return None

    return {
        "status": "issues_found" if all_issues else "clean",
        "summary": " | ".join(summaries) if summaries
                    else f"No issues found across {len(batches)} batch(es)",
        "issues": all_issues,
    }


